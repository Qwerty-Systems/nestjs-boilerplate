import { ApiProperty } from '@nestjs/swagger';

export class SystemModule {
  @ApiProperty({
    type: () => String,
    nullable: true,
  })
  submodules?: string | null;

  @ApiProperty({
    type: () => String,
    nullable: true,
  })
  description?: string | null;

  @ApiProperty({
    type: () => String,
    nullable: false,
  })
  name: string;

  @ApiProperty({
    type: String,
  })
  id: string;

  @ApiProperty()
  createdAt: Date;

  @ApiProperty()
  updatedAt: Date;
}
